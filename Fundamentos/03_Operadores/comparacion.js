/**
 * Operadores de Comparacion
 * Nota: retornan un valor logico
 */

// Igual (==)
console.log(3 == 3); // retorna un valor logico: true o false

// Diferente, no es igual (!=)
console.log(3 != 3); 

// estrictamente igual (===)
console.log(3 === '3');

// estrictamente diferente (!==)
console.log(3 !== '3');

// Menor que (<), mayor que (>), menor o igual que (<=), mayor o igual que (>=)
console.log(3 < 4);
console.log(3 > 4);
console.log(3 <= 4);
console.log(3 >= 4);