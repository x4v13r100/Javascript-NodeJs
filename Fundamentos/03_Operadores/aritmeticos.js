/**
 * Operadores Aritmeticos
 */

// Suma (+), Resta (-), Multiplicacion (*), Division (/), Modulo (%), Exponente (**)


// Incremento ++
var numero = 0;
console.log(++numero); // Aumenta el valor de la variable en 1 antes de la ejecucion
console.log(numero++); // Aumenta el valor de la variable en 1 despues de la ejecucion
console.log(numero);  // Aumenta el valor de la variable en 1

// Decremento --
var numero = 0;
console.log(--numero); // Aumenta el valor de la variable en 1 antes de la ejecucion
console.log(numero--); // Aumenta el valor de la variable en 1 despues de la ejecucion
console.log(numero);  // Aumenta el valor de la variable en 1